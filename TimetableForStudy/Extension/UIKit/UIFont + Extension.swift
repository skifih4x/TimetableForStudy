import UIKit

extension UIFont {

    //Avenir Next
    static func avenirNext20() -> UIFont? {
        UIFont.init(name: "Avenir Next", size: 20)
    }
    static func avenirNext14() -> UIFont? {
        UIFont.init(name: "Avenir Next", size: 14)
    }
    
    static func avenirNextDemiBold20() -> UIFont? {
        UIFont.init(name: "Avenir Next Demi Bold", size: 20)
    }
    static func avenirNextDemiBold14() -> UIFont? {
        UIFont.init(name: "Avenir NextDemi Bold", size: 14)
    }
}
